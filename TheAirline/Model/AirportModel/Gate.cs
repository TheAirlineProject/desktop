        }

        //returns all delivered gats
        public List<Gate> GetDeliveredGates()
        {
            return
                _terminalGates.FindAll(
                    (delegate(Gate gate) { return gate.DeliveryDate <= GameObject.GetInstance().GameTime; }));
        }

