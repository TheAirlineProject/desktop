using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TheAirline.Model.GeneralModel
{
    [System.AttributeUsage(System.AttributeTargets.Property |
                       System.AttributeTargets.Method | System.AttributeTargets.Field)]
    public class Versioning : System.Attribute
    {
        private string name;
        private double version;
        private object defaultvalue;
        private Boolean autogenerated;
        public Versioning(string name)
        {
            this.name = name;
            this.version = 1;
            this.autogenerated = true;
        }
        // This property is readonly (it has no set accessor)
        // so it cannot be used as a named argument to this attribute.
        public string Name
        {
            get
            {
                return name;
            }
        }

        public double Version
        {
            get
            {
                return version;
            }
            set
            {
                version = value;
            }
        }
        public Boolean AutoGenerated
        {
            get
            {
                return autogenerated;
            }
            set
            {
                autogenerated = value;
            }

        }
        public object DefaultValue
        {
            get
            {
                return defaultvalue;
            }
            set
            {
                defaultvalue = value;
            }
        }
    }
}